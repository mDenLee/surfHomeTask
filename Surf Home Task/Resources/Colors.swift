
import UIKit

enum Colors {
    
    // MARK: Colors
    
    static var backgroundColor: CGColor {
        return UIColor(red: 1, green: 1, blue: 1, alpha: 1).cgColor
    }
    
    static var darkGrayColor: UIColor {
        return UIColor(red: 49, green: 49, blue: 49, alpha: 1)
    }
    
    static var lightGrayColor: UIColor {
        return UIColor(red: 243, green: 243, blue: 245, alpha: 1)
    }
    
    static var joinButtonTextColor: UIColor {
        return UIColor(red: 255, green: 255, blue: 255, alpha: 1)
    }

}
